    private void atualizarTabelaCarro() {
        metodosDaTabelaCarro.setRowCount(0);
        for (Carro carro : carControler.listarCarrosCadastrados()){
            metodosDaTabelaCarro.addRow(new Object[] {carro.getMarca(), 
                carro.getModelo(), carro.getQtdPortas()});
        }
    }
    
    private void atualizarTabelaMoto() {
        metodosDaTabelaMoto.setRowCount(0);
        for (Moto moto : motoController.listarMotosCadastradas()){
            metodosDaTabelaMoto.addRow(new Object[] {moto.getMarca(),
            moto.getModelo(), moto.getCilindradas()});
        }
    }
    
