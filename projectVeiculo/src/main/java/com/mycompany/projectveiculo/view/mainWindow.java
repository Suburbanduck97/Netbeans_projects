    private void botaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastrarActionPerformed
        // TODO add your handling code here:
        String veiculo = tipoDeVeiculoBox.getSelectedItem().toString();
        String marca = marcaDoVeiculo.getText().toLowerCase();
        String modelo = modeloDoVeiculo.getText().toLowerCase();
        String qtdDePortas = campoQtdPortasDoCarro.getText();
        String cilindradas = campoCilindradasDaMoto.getText();
        
        if (veiculo.equals("Moto")){
            if (marca.isEmpty() || modelo.isEmpty() || cilindradas.isEmpty()){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos ou altere o tipo de veículo"
                    , "Atenção!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            try {
                int cilindradasDaMoto = Integer.parseInt(cilindradas.trim());

                if (cilindradasDaMoto > 2000){
                    JOptionPane.showMessageDialog(null, "Valor de cilindrada inválida", "Atenção",
                        JOptionPane.ERROR_MESSAGE);
                    return;
                }
            
                metodosDaTabelaMoto.setRowCount(0);
                ArrayList<Moto> listaDeMotos = motoController.listarMotosCadastradas();
                if (veiculo.equals("Moto")){
                    motoController.dataBaseControllerMotoAdicionar(marca, modelo, cilindradasDaMoto);
                    for (Moto moto : listaDeMotos){
                        metodosDaTabelaMoto.addRow(new Object[]{moto.getMarca(), moto.getModelo(), moto.getCilindradas()});
                    }
                    marcaDoVeiculo.setText("");
                    modeloDoVeiculo.setText("");
                    campoCilindradasDaMoto.setText("");
                }
            
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(null, "Digite um número válido!", "Atenção!",
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
        } else if (veiculo.equals("Carro")){
            
            if (marca.isEmpty() || modelo.isEmpty() || qtdDePortas.isEmpty()){
                JOptionPane.showMessageDialog(null, "Preencha todos os campos ou altere o tipo de veículo", 
                        "Atenção!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
            try {
                int qtdPortasDoCarro = Integer.parseInt(qtdDePortas.trim());
            
                if (qtdPortasDoCarro > 4 || qtdPortasDoCarro < 2){
                    JOptionPane.showMessageDialog(null, "Digite um número de portas entre 2 e 4", "Atenção!",
                        JOptionPane.ERROR_MESSAGE);
                    return;
                }
            
                metodosDaTabelaCarro.setRowCount(0);
                ArrayList<Carro> listaDeCarros = carControler.listarCarrosCadastrados();
                if (veiculo.equals("Carro")){
                    carControler.dataBaseControllerCarroAdicionar(marca, modelo, qtdPortasDoCarro);
                    for (Carro carro : listaDeCarros){
                        metodosDaTabelaCarro.addRow(new Object[] {carro.getMarca(), carro.getModelo(), carro.getQtdPortas()});
                    }
                
                    marcaDoVeiculo.setText("");
                    modeloDoVeiculo.setText("");
                    campoQtdPortasDoCarro.setText("");
                }
            
            
            } catch (NumberFormatException nfe) {
                JOptionPane.showMessageDialog(null, "Digite um número válido!", "Atenção",
                    JOptionPane.ERROR_MESSAGE);
                return;
            }
        }else{
            JOptionPane.showMessageDialog(null, "Tipo de veículo inválido!", "Atenção!",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoCadastrarActionPerformed

    private void botaoAlterarDadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarDadosActionPerformed
        // TODO add your handling code here:
        String tipoVeiculo = tipoDeVeiculoBox.getSelectedItem().toString();
        int linhaSelecionadaTblCarro = tabelaDeCarro.getSelectedRow();
        int linhaSelecionadaTblMoto = tabelaDeMoto.getSelectedRow();
        
        if (linhaSelecionadaTblCarro == -1 && linhaSelecionadaTblMoto == -1){
            JOptionPane.showMessageDialog(null, "Selecione um veículo para alterar.", "Atenção!",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (linhaSelecionadaTblCarro != -1 && linhaSelecionadaTblMoto != -1){
            JOptionPane.showMessageDialog(null, "Selecione a linha de somente uma tabela!",
                    "Atenção!", JOptionPane.ERROR_MESSAGE);
            atualizarTabelaCarro();
            atualizarTabelaMoto();
            return;
        }
        
        if (tipoVeiculo.equals("Carro")){
            if (linhaSelecionadaTblCarro != -1){
                metodosDaTabelaCarro.setValueAt(marcaDoVeiculo.getText(), linhaSelecionadaTblCarro, 0);
                metodosDaTabelaCarro.setValueAt(modeloDoVeiculo.getText(), linhaSelecionadaTblCarro , 1);
                metodosDaTabelaCarro.setValueAt(campoQtdPortasDoCarro.getText(), linhaSelecionadaTblCarro, 2);
                JOptionPane.showMessageDialog(null, "Atualização concluída com sucesso!", 
                    "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                    return;
            }else{
                JOptionPane.showMessageDialog(null, "Altere o tipo de veículo!",
                        "Atenção!", JOptionPane.ERROR_MESSAGE);
                    return;
            }
        }else if (tipoVeiculo.equals("Moto")){
            if (linhaSelecionadaTblMoto != -1){
                metodosDaTabelaMoto.setValueAt(marcaDoVeiculo.getText(), linhaSelecionadaTblMoto, 0);
                metodosDaTabelaMoto.setValueAt(modeloDoVeiculo.getText(), linhaSelecionadaTblMoto, 1);
                metodosDaTabelaMoto.setValueAt(campoCilindradasDaMoto.getText(), linhaSelecionadaTblMoto, 2);
                JOptionPane.showMessageDialog(null, "Atualização concluída com sucesso!", 
                    "Atenção!", JOptionPane.INFORMATION_MESSAGE);
                    return;
            }else{
                JOptionPane.showMessageDialog(null, "Altere o tipo de veículo!",
                        "Atenção!", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
    }//GEN-LAST:event_botaoAlterarDadosActionPerformed

