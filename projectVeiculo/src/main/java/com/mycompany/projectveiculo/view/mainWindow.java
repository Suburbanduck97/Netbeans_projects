    private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
        // TODO add your handling code here:
        int linhaSelecionadaTbCarro = tabelaDeCarro.getSelectedRow();
        int linhaSelecionadaTblMoto = tabelaDeMoto.getSelectedRow();
        
        if (linhaSelecionadaTbCarro == -1 && linhaSelecionadaTblMoto == -1){
            JOptionPane.showMessageDialog(null, "Selecione uma das linhas da tabela para remover",
                    "Atenção", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (linhaSelecionadaTbCarro != -1){
            ArrayList<Carro> listaDeCarros = carControler.listarCarrosCadastrados();
            listaDeCarros.remove(linhaSelecionadaTbCarro);
            atualizarTabelaCarro();
            limparCampos();
            JOptionPane.showMessageDialog(null, "Linha removida com sucesso!", "Atenção!",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        if (linhaSelecionadaTblMoto != -1){
            ArrayList<Moto> listaDeMotos = motoController.listarMotosCadastradas();
            listaDeMotos.remove(linhaSelecionadaTblMoto);
            atualizarTabelaMoto();
            limparCampos();
            JOptionPane.showMessageDialog(null, "Linha removida com sucesso!", "Atenção!",
                    JOptionPane.INFORMATION_MESSAGE);
            return;
        }
    }//GEN-LAST:event_botaoRemoverActionPerformed

